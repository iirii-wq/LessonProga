N = int(input('Введите сколько предметов может унести игрок: '))
items = list(map(int, input('Ценности всех найденых предметов (через пробел): ').split()))

# Сортируем предметы по убыванию ценности
items.sort(reverse=True)

# Берем первые N предметов (или все, если их меньше N)
selected_items = items[:N]

# Суммируем только положительные ценности, так как отрицательные брать невыгодно
result = sum(item for item in selected_items if item > 0)

# Выводим результат
print('Максимально возможная суммарная ценнлсть предметов, которые можно  взять: ',result)